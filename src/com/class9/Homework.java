public class Homework {

	public static void main(String[] args) {

		for(int a=5; a>=1; a--) {
			for(int b=1; b<=a; b++) {
				System.out.print(a);
			}
			System.out.println();
		}
		
		for(int c=1;c<=5;c++) {
			for(int d=1; d<=c; d++) {
				System.out.print("*");
			}
			System.out.println();
		}
		for (int e=4; e>=1; e--) {
			for (int f=1; f<=e;f++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		
	}

}
